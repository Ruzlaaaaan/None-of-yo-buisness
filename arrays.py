#Insert specific number ----- .insert(index, value)
numbers = [1,2,3,4]
numbers.insert(1, 10)
print(numbers)

#Removing elements
numbers.remove(10)
print(numbers)

#Remove element by index
del numbers[0]
print(numbers)

print(sum(numbers))
print(len(numbers))
print(max(numbers))
print(min(numbers))



